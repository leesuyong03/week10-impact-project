import numpy as np
import matplotlib.pyplot as plt

# =========================================================
# 한글 폰트 설정
# =========================================================
plt.rcParams['font.family'] = 'Malgun Gothic'
plt.rcParams['axes.unicode_minus'] = False

# =========================================================
# 실험 조건 설정
# =========================================================
g = 9.81                  # 중력가속도 (m/s²)
h = 1.0                   # 낙하 높이 (m)

mass = 0.25               # 과일 질량 (kg)

t_total = 1.2             # 전체 시간
points = 3000

# =========================================================
# 반발 계수 및 충돌 시간
# =========================================================

# 맨바닥
e_hard = 0.34
dt_hard = 0.005

# 완충재
e_soft = 0.18
dt_soft = 0.020

# =========================================================
# 시간 배열 생성
# =========================================================
time = np.linspace(0, t_total, points)

# =========================================================
# 위치 및 가속도 계산 함수
# =========================================================
def simulate_motion(e, dt):

    # 자유낙하 시간
    t_fall = np.sqrt(2 * h / g)

    # 충돌 직전 속도
    v1 = -np.sqrt(2 * g * h)

    # 충돌 직후 속도
    v2 = abs(v1) * e

    y = []
    a = []

    for t in time:

        # =================================================
        # 자유낙하 구간
        # =================================================
        if t < t_fall:

            yt = h - 0.5 * g * t**2

            y.append(yt)

            # 중력가속도
            a.append(-g)

        # =================================================
        # 충돌 구간
        # =================================================
        elif t < t_fall + dt:

            y.append(0)

            # 충돌 가속도
            impact_acc = (v2 - v1) / dt

            a.append(impact_acc)

        # =================================================
        # 반등 구간
        # =================================================
        else:

            t_bounce = t - (t_fall + dt)

            yt = v2 * t_bounce - 0.5 * g * t_bounce**2

            if yt > 0:

                y.append(yt)

                a.append(-g)

            else:

                y.append(0)

                a.append(0)

    # 반드시 4개 반환
    return np.array(y), np.array(a), v1, v2

# =========================================================
# 데이터 계산
# =========================================================

# 맨바닥
y_hard, a_hard, v1_h, v2_h = simulate_motion(
    e_hard,
    dt_hard
)

# 완충재
y_soft, a_soft, v1_s, v2_s = simulate_motion(
    e_soft,
    dt_soft
)

# =========================================================
# 충격력 계산
# =========================================================
F_hard = mass * (v2_h - v1_h) / dt_hard

F_soft = mass * (v2_s - v1_s) / dt_soft

# 손상 임계치
damage_threshold = 150

# =========================================================
# 결과 출력
# =========================================================
print('==============================')
print('과일 낙하 충격 분석 결과')
print('==============================')

print(f'맨바닥 반발계수 e : {e_hard:.2f}')
print(f'완충재 반발계수 e : {e_soft:.2f}')

print(f'맨바닥 최대 충격력 : {F_hard:.2f} N')
print(f'완충재 최대 충격력 : {F_soft:.2f} N')

print('------------------------------')

if F_hard > damage_threshold:
    print('맨바닥 결과 : 손상 발생 가능성 높음')
else:
    print('맨바닥 결과 : 안전')

if F_soft > damage_threshold:
    print('완충재 결과 : 손상 발생 가능성 높음')
else:
    print('완충재 결과 : 안전')

# =========================================================
# 그래프 생성
# =========================================================
fig, (ax1, ax2) = plt.subplots(
    2,
    1,
    figsize=(12, 10)
)

# =========================================================
# 위치-시간 그래프
# =========================================================
ax1.plot(
    time,
    y_hard,
    color='red',
    linewidth=2.5,
    label='맨바닥'
)

ax1.plot(
    time,
    y_soft,
    color='green',
    linewidth=2.5,
    label='완충재'
)

# 그래프 색 채우기
ax1.fill_between(
    time,
    y_hard,
    color='red',
    alpha=0.08
)

ax1.fill_between(
    time,
    y_soft,
    color='green',
    alpha=0.08
)

ax1.set_title(
    '위치-시간 그래프',
    fontsize=20
)

ax1.set_xlabel('시간 (s)')
ax1.set_ylabel('높이 (m)')

ax1.grid(True)

ax1.legend()

# =========================================================
# 가속도-시간 그래프
# =========================================================
ax2.plot(
    time,
    a_hard,
    color='red',
    linewidth=2.5,
    label='맨바닥 가속도'
)

ax2.plot(
    time,
    a_soft,
    color='green',
    linewidth=2.5,
    label='완충재 가속도'
)

# 손상 임계치
ax2.axhline(
    damage_threshold,
    color='orange',
    linestyle='--',
    linewidth=2.5,
    label='손상 임계치'
)

# 임계치 영역 색칠
ax2.fill_between(
    time,
    damage_threshold,
    1500,
    color='orange',
    alpha=0.08
)

ax2.set_title(
    '가속도-시간 그래프',
    fontsize=20
)

ax2.set_xlabel('시간 (s)')
ax2.set_ylabel('가속도 (m/s²)')

ax2.grid(True)

ax2.legend()

# =========================================================
# 그래프 간격 자동 조절
# =========================================================
plt.tight_layout()

# =========================================================
# 그래프 저장
# =========================================================
plt.savefig(
    'impact_analysis_result.png',
    dpi=300,
    bbox_inches='tight'
)

# =========================================================
# 그래프 출력
# =========================================================
plt.show()
